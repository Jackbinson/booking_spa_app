const config = require('../src/config/env');

const { getAuth, getFirebaseProjectId } = require('../src/config/firebase');
const { categories, demoUser, services } = require('./firebase-data');
const { upsertDocument } = require('./firestore-rest');

const now = () => new Date();

async function upsertCollection(collectionName, records) {
  for (const record of records) {
    await upsertDocument(collectionName, record.id, {
      ...record,
      updatedAt: now(),
      createdAt: now(),
    });
  }

  return records.length;
}

async function seedAdminUser() {
  if (!config.firebase.seedAdminEmail || !config.firebase.seedAdminPassword) {
    return null;
  }

  const auth = getAuth();
  let user;

  try {
    user = await auth.getUserByEmail(config.firebase.seedAdminEmail);
  } catch (error) {
    if (error.code !== 'auth/user-not-found') {
      throw error;
    }

    user = await auth.createUser({
      email: config.firebase.seedAdminEmail,
      password: config.firebase.seedAdminPassword,
      displayName: 'System Admin',
      emailVerified: true,
    });
  }

  await auth.setCustomUserClaims(user.uid, { role: 'admin' });
  await upsertDocument('users', user.uid, {
    id: user.uid,
    fullName: 'System Admin',
    email: config.firebase.seedAdminEmail,
    role: 'admin',
    isActive: true,
    updatedAt: now(),
    createdAt: now(),
  });

  return user.uid;
}

async function seedDemoAppointment() {
  const appointmentDate = new Date(Date.now() + 24 * 60 * 60 * 1000);

  await upsertDocument('appointments', 'demo-pending-appointment', {
    id: 'demo-pending-appointment',
    customerId: demoUser.id,
    customerName: demoUser.fullName,
    serviceId: services[0].id,
    serviceName: services[0].name,
    startAt: appointmentDate,
    durationMinutes: services[0].durationMinutes,
    totalPrice: services[0].price,
    status: 'pending',
    note: 'Demo booking generated by seed script.',
    updatedAt: now(),
    createdAt: now(),
  });
}

async function main() {
  const projectId = getFirebaseProjectId();

  if (!projectId) {
    throw new Error('Missing FIREBASE_PROJECT_ID or project_id in service account.');
  }

  const categoryCount = await upsertCollection('serviceCategories', categories);
  const serviceCount = await upsertCollection('services', services);
  await upsertCollection('users', [demoUser]);
  await seedDemoAppointment();
  const adminUid = await seedAdminUser();

  console.log(`Seeded Firestore project: ${projectId}`);
  console.log(`serviceCategories: ${categoryCount}`);
  console.log(`services: ${serviceCount}`);
  console.log('users: 1 demo customer');
  console.log('appointments: 1 demo pending appointment');

  if (adminUid) {
    console.log(`admin auth user: ${adminUid}`);
  }
}

main()
  .then(() => process.exit(0))
  .catch((error) => {
    console.error(error.stack || error.message);
    process.exit(1);
  });
